#include "../../../includes/libft.h"

size_t	ft_strlen(const char *S)
{
	char	*tmp;

	tmp = NULL;
	if (!S)
		return (0);
	tmp = (char *) S;
	while (*tmp)
		tmp++;
	return ((size_t)(tmp - S));
}
